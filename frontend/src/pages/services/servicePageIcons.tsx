export function BentoTitle({
  segments,
}: {
  segments: readonly { text: string; variant: 'default' | 'accent' | 'accentBlock' }[]
}): ReactNode {
  return (
    <>
      {segments.map((s, i) => {
        if (s.variant === 'accentBlock') {
          return (
            <span key={i} className="block text-[#00429d]">
              {s.text}
            </span>
          )
        }
        if (s.variant === 'accent') {
          return (
            <span key={i} className="text-[#00429d]">
              {s.text}
            </span>
          )
        }
        return (
          <span key={i} className="text-[#191c1d]">
            {s.text}
          </span>
        )
      })}
    </>
  )
}
